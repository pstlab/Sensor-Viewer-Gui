package it.cnr.istc.hsv.abstracts.extra;

/**
 *
 * @author Luca
 */
public class Utils {

    public static <T> boolean contains(final T[] array, final T v) {
        for (final T e : array) {
            if (e == v || v != null && v.equals(e)) {
                return true;
            }
        }

        return false;
    }
}
